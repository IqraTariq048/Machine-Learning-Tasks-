def get_influential_words(email_text, feature_vectorizer, model_coefficients, feature_names, top_n=10):
    """
    Identifies words in the email text that strongly contributed to the classification
    based on model coefficients.
    """
    # Preprocess the email text to extract individual words
    words = re.findall(r'\b[a-z]+\b', email_text.lower())

    # Create a mapping from feature name to its coefficient
    coef_dict = dict(zip(feature_names, model_coefficients))

    # Collect words from the email that are in the model's vocabulary and their coefficients
    email_word_coefficients = []
    for word in set(words): # Use set to avoid duplicate processing for repeated words
        if word in coef_dict:
            email_word_coefficients.append((word, coef_dict[word]))

    # Sort words by coefficient value
    email_word_coefficients.sort(key=lambda x: x[1], reverse=True)

    # Top N words most indicative of spam (highest positive coefficients)
    spam_words = [word for word, coef in email_word_coefficients if coef > 0][:top_n]

    # Top N words most indicative of not spam (lowest negative coefficients)
    # Filter for negative coefficients first, then sort by absolute value or just take the lowest
    ham_words = [word for word, coef in email_word_coefficients if coef < 0]
    ham_words.sort(key=lambda x: x[1]) # Sort in ascending order for lowest coefficients
    ham_words = [word for word, coef in ham_words][:top_n]

    return spam_words, ham_words


