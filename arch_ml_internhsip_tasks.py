def predict_spam(preprocessed_email_vector):
    """
    Predicts whether a preprocessed email feature vector is spam or not spam,
    and returns the human-readable prediction along with the prediction probability.
    """
    # Make prediction using the trained model
    prediction_class = model.predict(preprocessed_email_vector)[0]

    # Get prediction probabilities for both classes
    prediction_proba = model.predict_proba(preprocessed_email_vector)[0]

    # Probability of being spam (class 1)
