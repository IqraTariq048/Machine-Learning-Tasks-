"""## Implement File Upload for Emails

### Subtask:
Add an ipywidgets FileUpload component to allow users to upload .txt or .eml files. Develop functions to read and extract the main text content from these uploaded files.

**Reasoning**:
I need to define the `read_uploaded_file` function as specified, which will handle decoding `.txt` files and parsing `.eml` files to extract their text content. This function will include error handling for robust file processing.
"""

import email

def read_uploaded_file(uploaded_file_value):
    """
    Reads the content of an uploaded file (txt or eml) and extracts the main text.
    Expects the value from an ipywidgets.FileUpload widget.
    """
    if not uploaded_file_value:
        return None, "No file uploaded."

    # Assuming only one file is uploaded due to multiple=False
    file_info = list(uploaded_file_value.values())[0]
    file_name = file_info['metadata']['name']
    content = file_info['content']

    try:
        if file_name.endswith('.txt'):
            text_content = content.decode('utf-8')
            return text_content, None
        elif file_name.endswith('.eml'):
            # Parse the email content
            msg = email.message_from_bytes(content)
            main_text = ""
            if msg.is_multipart():
                for part in msg.walk():
                    ctype = part.get_content_type()
                    cdisp = part.get('Content-Disposition')

                    # Look for plain text parts without attachments
                    if ctype == 'text/plain' and cdisp is None:
                        try:
                            charset = part.get_content_charset() or 'utf-8'
                            main_text += part.get_payload(decode=True).decode(charset)
                        except UnicodeDecodeError:
                            main_text += part.get_payload(decode=True).decode('latin-1') # Fallback
            else:
                # Not multipart, just get payload
                ctype = msg.get_content_type()
                if ctype == 'text/plain':
                    try:
                        charset = msg.get_content_charset() or 'utf-8'
                        main_text = msg.get_payload(decode=True).decode(charset)
                    except UnicodeDecodeError:
                        main_text = msg.get_payload(decode=True).decode('latin-1') # Fallback
            if main_text:
                return main_text, None
            else:
                return None, f"Could not extract main text from .eml file: {file_name}."
        else:
            return None, f"Unsupported file type: {file_name}. Only .txt and .eml are supported."
    except Exception as e:
        return None, f"Error processing file '{file_name}': {e}"

